// === UNBURNED POINTS (500 from high-NDVI pre-fire areas) ===
var s2 = ee.ImageCollection('COPERNICUS/S2_SR_HARMONIZED')
  .filterBounds(muglaGeom)
  .filterDate('2025-05-01', '2025-06-15')
  .filter(ee.Filter.lt('CLOUDY_PIXEL_PERCENTAGE', 20))
  .map(function(img) {
    var qa = img.select('QA60');
    var mask = qa.bitwiseAnd(1<<10).eq(0).and(qa.bitwiseAnd(1<<11).eq(0));
    return img.updateMask(mask).divide(10000);
  }).median().clip(muglaGeom);

var ndvi = s2.normalizedDifference(['B8','B4']).rename('NDVI');

// NDVI > 0.3 = healthy vegetation = confirmed unburned
var unburnedMask = ndvi.gt(0.3).selfMask();

var unburnedPoints = unburnedMask.sample({
  region: muglaGeom,
  scale: 100,
  numPixels: 500,
  seed: 42,
  geometries: true
}).map(function(f) { return f.set('Label', 0); });

